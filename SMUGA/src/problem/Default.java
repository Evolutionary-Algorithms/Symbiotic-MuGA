public class Default extends DeceptivePairIntertwined {

    @Override
    public String toString() {
        return "(" + getClass().getSuperclass().getSimpleName() + ")" + super.toString();
    }

    @Override
    public String getName() {
        return "(" + getClass().getSuperclass().getSimpleName() + ")" + super.getName();
    }
}
